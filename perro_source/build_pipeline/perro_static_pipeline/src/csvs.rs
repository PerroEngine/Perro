use crate::{
    StaticPipelineError, asset_uri, ensure_unique_hashes, escape_rust_str, res_dir, static_dir,
    write_hash_const,
};
use perro_ids::string_to_u64;
use std::{fmt::Write as _, fs, path::Path};

pub fn generate_static_csvs(project_root: &Path) -> Result<(), StaticPipelineError> {
    let static_dir = static_dir(project_root);
    fs::create_dir_all(&static_dir)?;

    let root = res_dir(project_root);
    let mut paths = Vec::<(String, std::path::PathBuf)>::new();
    collect_csv_paths(&root, &root, &mut paths)?;
    paths.sort_by(|a, b| a.0.cmp(&b.0));
    ensure_unique_hashes("csv", paths.iter().map(|(path, _)| path.as_str()))?;

    let mut out = String::new();
    out.push_str("// Auto-generated by Perro Static Pipeline. Do not edit.\n");
    out.push_str("#![allow(unused_imports)]\n\n");
    out.push_str("use perro_csv::{CsvCell, CsvRow, CsvRowIndex, PerroCsv};\n\n");
    out.push_str("static EMPTY_CSV: PerroCsv = PerroCsv::empty();\n\n");

    for (table_index, (asset_path, disk_path)) in paths.iter().enumerate() {
        write_hash_const(&mut out, &format!("CSV_HASH_{table_index}"), asset_path);
        emit_csv_table(&mut out, table_index, asset_path, disk_path)?;
    }

    out.push_str("pub const fn lookup_csv(path_hash: u64) -> &'static PerroCsv {\n");
    out.push_str("    match path_hash {\n");
    for (table_index, _) in paths.iter().enumerate() {
        let _ = writeln!(out, "        CSV_HASH_{table_index} => &CSV_{table_index},");
    }
    out.push_str("        _ => &EMPTY_CSV,\n");
    out.push_str("    }\n");
    out.push_str("}\n");

    fs::write(static_dir.join("csvs.rs"), out)?;
    Ok(())
}

fn collect_csv_paths(
    root: &Path,
    dir: &Path,
    out: &mut Vec<(String, std::path::PathBuf)>,
) -> Result<(), StaticPipelineError> {
    if !dir.exists() {
        return Ok(());
    }
    for entry in fs::read_dir(dir)? {
        let entry = entry?;
        let path = entry.path();
        if path.is_dir() {
            collect_csv_paths(root, &path, out)?;
            continue;
        }
        if path.extension().and_then(|ext| ext.to_str()) != Some("csv") {
            continue;
        }
        let rel = path
            .strip_prefix(root)
            .map_err(|err| StaticPipelineError::SceneParse(err.to_string()))?
            .to_string_lossy()
            .replace('\\', "/");
        out.push((asset_uri(&rel), path));
    }
    Ok(())
}

fn emit_csv_table(
    out: &mut String,
    table_index: usize,
    asset_path: &str,
    disk_path: &Path,
) -> Result<(), StaticPipelineError> {
    let bytes = fs::read(disk_path)?;
    let mut reader = csv::ReaderBuilder::new()
        .has_headers(true)
        .from_reader(bytes.as_slice());
    let headers = reader.headers().map_err(|err| {
        StaticPipelineError::SceneParse(format!("failed to parse csv `{asset_path}`: {err}"))
    })?;
    let header_values: Vec<String> = headers
        .iter()
        .map(|value| value.trim().to_string())
        .collect();
    emit_cells(out, &format!("CSV_{table_index}_HEADERS"), &header_values);

    let mut row_count = 0usize;
    let mut primary_index = Vec::<(u64, usize, String)>::new();
    let mut seen_primary = std::collections::HashSet::<u64>::new();
    for row in reader.records() {
        let row = row.map_err(|err| {
            StaticPipelineError::SceneParse(format!("failed to parse csv `{asset_path}`: {err}"))
        })?;
        let values: Vec<String> = row.iter().map(|value| value.trim().to_string()).collect();
        emit_cells(
            out,
            &format!("CSV_{table_index}_ROW_{row_count}_CELLS"),
            &values,
        );
        if let Some(first) = values.first() {
            let hash = string_to_u64(first);
            if seen_primary.insert(hash) {
                primary_index.push((hash, row_count, first.clone()));
            }
        }
        row_count += 1;
    }

    let _ = writeln!(
        out,
        "static CSV_{table_index}_ROWS: [CsvRow; {row_count}] = ["
    );
    for row_index in 0..row_count {
        let _ = writeln!(
            out,
            "    CsvRow::new(&CSV_{table_index}_ROW_{row_index}_CELLS),"
        );
    }
    out.push_str("];\n\n");

    primary_index.sort_by_key(|entry| entry.0);
    let _ = writeln!(
        out,
        "static CSV_{table_index}_PRIMARY_INDEX: [CsvRowIndex; {}] = [",
        primary_index.len()
    );
    for (_hash, row, key) in primary_index {
        let _ = writeln!(
            out,
            "    CsvRowIndex::new(perro_ids::hash_str!(\"{}\"), {row}),",
            escape_rust_str(&key)
        );
    }
    out.push_str("];\n\n");

    let _ = writeln!(
        out,
        "static CSV_{table_index}: PerroCsv = PerroCsv::new(&CSV_{table_index}_HEADERS, &CSV_{table_index}_ROWS, &CSV_{table_index}_PRIMARY_INDEX);\n"
    );
    Ok(())
}

fn emit_cells(out: &mut String, name: &str, values: &[String]) {
    let _ = writeln!(out, "static {name}: [CsvCell; {}] = [", values.len());
    for value in values {
        let escaped = escape_rust_str(value);
        let _ = writeln!(
            out,
            "    CsvCell::new(\"{escaped}\", perro_ids::hash_str!(\"{escaped}\")),"
        );
    }
    out.push_str("];\n\n");
}
