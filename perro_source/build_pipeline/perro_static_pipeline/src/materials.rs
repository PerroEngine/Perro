pub fn generate_static_materials(project_root: &Path) -> Result<(), StaticPipelineError> {
    let res_dir = res_dir(project_root);
    let static_dir = static_dir(project_root);
    fs::create_dir_all(&static_dir)?;

    let mut material_paths = Vec::<String>::new();
    if res_dir.exists() {
        material_paths = collect_file_paths(&res_dir, &res_dir)?
            .into_iter()
            .map(|rel| rel.replace('\\', "/"))
            .filter(|rel| {
                Path::new(rel)
                    .extension()
                    .and_then(|e| e.to_str())
                    .is_some_and(|ext| source_ext::contains(source_ext::MATERIAL_INPUT, ext))
            })
            .collect();
    }
    material_paths.sort();

    let mut materials = material_paths
        .into_par_iter()
        .map(|rel| -> io::Result<Vec<(String, MaterialLiteral, bool)>> {
            let res_path = asset_uri(&rel);
            let full_path = res_dir.join(&rel);
            let ext = Path::new(&rel)
                .extension()
                .and_then(|e| e.to_str())
                .map(|ext| ext.to_ascii_lowercase())
                .unwrap_or_default();
            match ext.as_str() {
                source_ext::MATERIAL => {
                    let src = fs::read_to_string(&full_path)?;
                    let material = load_pmat_literal(&src).ok_or_else(|| {
                        io::Error::other(format!(
                            "material `{res_path}` must be an object with at least one valid field"
                        ))
                    })?;
                    Ok(vec![(res_path, material, false)])
                }
                source_ext::GLB | source_ext::GLTF => {
                    materials_from_gltf_file(&full_path, &res_path)
                }
                _ => Ok(Vec::new()),
            }
        })
        .collect::<io::Result<Vec<_>>>()?
        .into_iter()
        .flatten()
        .collect::<Vec<_>>();

    materials.sort_by(|a, b| a.0.cmp(&b.0));

    let mut unique_materials = Vec::<MaterialLiteral>::new();
    let mut unique_by_key = HashMap::<MaterialKey, usize>::new();
    let mut material_refs = Vec::<(String, usize, bool)>::with_capacity(materials.len());
    for (path, material, synthesized) in materials {
        let key = MaterialKey::from(&material);
        let index = if let Some(existing) = unique_by_key.get(&key).copied() {
            existing
        } else {
            let idx = unique_materials.len();
            unique_materials.push(material);
            unique_by_key.insert(key, idx);
            idx
        };
        material_refs.push((path, index, synthesized));
    }
    ensure_unique_hashes(
        "material",
        material_refs.iter().map(|(path, _, _)| path.as_str()),
    )?;

    let mut out = String::new();
    out.push_str("// Auto-generated by Perro Static Pipeline. Do not edit.\n");
    out.push_str("#![allow(unused_imports)]\n");
    out.push_str(
        "use perro_render_bridge::{CustomMaterial3D, CustomMaterialImage3D, CustomMaterialLighting3D, CustomMaterialParam3D, CustomMaterialParamValue3D, Material3D, StandardMaterial3D, ToonMaterial3D, UnlitMaterial3D};\nuse std::borrow::Cow;\n\n",
    );
    for (index, material) in unique_materials.iter().enumerate() {
        let const_name = format!("MATERIAL_{}", index);
        let _ = writeln!(
            out,
            "static {const_name}: Material3D = {};",
            material_literal_to_code(material)
        );
    }
    out.push('\n');
    out.push_str(
        "static EMPTY_MATERIAL: Material3D = Material3D::Standard(StandardMaterial3D::const_default());\n\n",
    );
    for (index, (path, _, _)) in material_refs.iter().enumerate() {
        write_hash_const(&mut out, &format!("MATERIAL_HASH_{index}"), path);
    }
    if !material_refs.is_empty() {
        out.push('\n');
    }
    let lookup_entries = material_refs
        .iter()
        .enumerate()
        .map(|(hash_index, (path, index, _))| {
            (
                perro_ids::string_to_u64(path),
                format!("MATERIAL_HASH_{hash_index}"),
                format!("&MATERIAL_{index}"),
            )
        })
        .collect::<Vec<_>>();
    write_static_lookup_fn(
        &mut out,
        "lookup_material",
        "MATERIAL_TABLE",
        "MaterialEntry",
        "&'static Material3D",
        "&EMPTY_MATERIAL",
        &lookup_entries,
    );

    crate::write_if_changed(&static_dir.join("materials.rs"), out.as_bytes())?;
    crate::record_static_assets(
        perro_asset_formats::dlc::DlcAssetKind::MATERIAL,
        perro_asset_formats::dlc::DlcAssetAccess::ENGINE_LOCAL,
        material_refs
            .iter()
            .map(|(path, _, synthesized)| (path.as_str(), *synthesized)),
    );
    Ok(())
}

