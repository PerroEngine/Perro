use crate::{StaticPipelineError, asset_uri, ensure_unique_hashes, res_dir, static_dir};
use perro_io::walkdir::collect_file_paths;
use perro_render_bridge::{
    CustomMaterialParamValue3D, StandardMaterial3D, ToonMaterial3D, UnlitMaterial3D,
};
use perro_scene::{Parser, SceneObjectField, SceneValue};
use rayon::prelude::*;
use std::{borrow::Cow, collections::HashMap, fmt::Write as _, fs, io, path::Path};

pub fn generate_static_materials(project_root: &Path) -> Result<(), StaticPipelineError> {
    let res_dir = res_dir(project_root);
    let static_dir = static_dir(project_root);
    fs::create_dir_all(&static_dir)?;

    let mut material_paths = Vec::<String>::new();
    if res_dir.exists() {
        material_paths = collect_file_paths(&res_dir, &res_dir)?
            .into_iter()
            .map(|rel| rel.replace('\\', "/"))
            .filter(|rel| {
                Path::new(rel)
                    .extension()
                    .and_then(|e| e.to_str())
                    .map(|ext| ext.to_ascii_lowercase())
                    .is_some_and(|ext| matches!(ext.as_str(), "pmat" | "glb" | "gltf"))
            })
            .collect();
    }
    material_paths.sort();

    let mut materials = material_paths
        .into_par_iter()
        .map(|rel| -> io::Result<Vec<(String, MaterialLiteral)>> {
            let res_path = asset_uri(&rel);
            let full_path = res_dir.join(&rel);
            let ext = Path::new(&rel)
                .extension()
                .and_then(|e| e.to_str())
                .map(|ext| ext.to_ascii_lowercase())
                .unwrap_or_default();
            match ext.as_str() {
                "pmat" => {
                    let src = fs::read_to_string(&full_path)?;
                    let material = load_pmat_literal(&src).ok_or_else(|| {
                        io::Error::other(format!(
                            "material `{res_path}` must be an object with at least one valid field"
                        ))
                    })?;
                    Ok(vec![(res_path, material)])
                }
                "glb" | "gltf" => materials_from_gltf_file(&full_path, &res_path),
                _ => Ok(Vec::new()),
            }
        })
        .collect::<io::Result<Vec<_>>>()?
        .into_iter()
        .flatten()
        .collect::<Vec<_>>();

    materials.sort_by(|a, b| a.0.cmp(&b.0));

    let mut unique_materials = Vec::<MaterialLiteral>::new();
    let mut unique_by_key = HashMap::<MaterialKey, usize>::new();
    let mut material_refs = Vec::<(String, usize)>::with_capacity(materials.len());
    for (path, material) in materials {
        let key = MaterialKey::from(&material);
        let index = if let Some(existing) = unique_by_key.get(&key).copied() {
            existing
        } else {
            let idx = unique_materials.len();
            unique_materials.push(material);
            unique_by_key.insert(key, idx);
            idx
        };
        material_refs.push((path, index));
    }
    ensure_unique_hashes(
        "material",
        material_refs.iter().map(|(path, _)| path.as_str()),
    )?;

    let mut out = String::new();
    out.push_str("// Auto-generated by Perro Static Pipeline. Do not edit.\n");
    out.push_str("#![allow(unused_imports)]\n");
    out.push_str(
        "use perro_render_bridge::{CustomMaterial3D, CustomMaterialParam3D, CustomMaterialParamValue3D, Material3D, StandardMaterial3D, ToonMaterial3D, UnlitMaterial3D};\nuse std::borrow::Cow;\n\n",
    );
    for (index, material) in unique_materials.iter().enumerate() {
        let const_name = format!("MATERIAL_{}", index);
        let _ = writeln!(
            out,
            "static {const_name}: Material3D = {};",
            material_literal_to_code(material)
        );
    }
    out.push('\n');
    out.push_str(
        "static EMPTY_MATERIAL: Material3D = Material3D::Standard(StandardMaterial3D::const_default());\n\n",
    );
    out.push_str("pub const fn lookup_material(path_hash: u64) -> &'static Material3D {\n");
    out.push_str("    match path_hash {\n");
    for (path, index) in &material_refs {
        let path_hash = perro_ids::string_to_u64(path);
        let _ = writeln!(out, "        {path_hash}u64 => &MATERIAL_{},", index,);
    }
    out.push_str("        _ => &EMPTY_MATERIAL,\n");
    out.push_str("    }\n");
    out.push_str("}\n");

    fs::write(static_dir.join("materials.rs"), out)?;
    Ok(())
}

#[derive(Clone)]
enum MaterialLiteral {
    Standard(StandardMaterial3D),
    Unlit(UnlitMaterial3D),
    Toon(ToonMaterial3D),
    Custom(CustomMaterialLiteral),
}

