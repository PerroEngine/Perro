use crate::{StaticPipelineError, static_dir};
use std::{fs, path::Path};

pub fn generate_static_animations(project_root: &Path) -> Result<(), StaticPipelineError> {
    let static_dir = static_dir(project_root);
    fs::create_dir_all(&static_dir)?;
    let src = "// Auto-generated by Perro Static Pipeline. Do not edit.\n#![allow(unused_imports)]\n\nuse perro_animation::AnimationClip;\n\npub fn lookup_animation(_path: &str) -> Option<&'static AnimationClip> {\n    None\n}\n";
    fs::write(static_dir.join("animations.rs"), src)?;
    Ok(())
}
