use crate::{StaticPipelineError, embedded_dir, res_dir, static_dir};
use perro_io::walkdir::collect_file_paths;
use rayon::prelude::*;
use std::{fmt::Write as _, fs, io, path::Path};

#[derive(Clone)]
struct AnimRef {
    lookup_key: String,
    embedded_rel_path: String,
}

pub fn generate_static_animations(project_root: &Path) -> Result<(), StaticPipelineError> {
    let res_dir = res_dir(project_root);
    let static_dir = static_dir(project_root);
    let embedded_animations_dir = embedded_dir(project_root).join("animations");
    fs::create_dir_all(&static_dir)?;
    fs::create_dir_all(&embedded_animations_dir)?;

    let mut anim_paths = Vec::<String>::new();
    if res_dir.exists() {
        anim_paths = collect_file_paths(&res_dir, &res_dir)?
            .into_iter()
            .map(|rel| rel.replace('\\', "/"))
            .filter(|rel| Path::new(rel).extension().and_then(|e| e.to_str()) == Some("panim"))
            .collect();
    }
    anim_paths.sort();

    let mut refs = anim_paths
        .par_iter()
        .map(|rel| -> io::Result<AnimRef> {
            let source = res_dir.join(rel);
            let text = fs::read_to_string(&source)?;
            perro_animation::parse_panim(&text).map_err(io::Error::other)?;

            let output = embedded_animations_dir.join(rel);
            if let Some(parent) = output.parent() {
                fs::create_dir_all(parent)?;
            }
            fs::write(&output, text)?;

            Ok(AnimRef {
                lookup_key: format!("res://{rel}"),
                embedded_rel_path: rel.clone(),
            })
        })
        .collect::<io::Result<Vec<_>>>()?;

    refs.sort_by(|a, b| a.lookup_key.cmp(&b.lookup_key));

    let mut src = String::new();
    src.push_str("// Auto-generated by Perro Static Pipeline. Do not edit.\n");
    src.push_str("#![allow(unused_imports)]\n#![allow(dead_code)]\n\n");
    src.push_str("use perro_animation::{AnimationClip, parse_panim};\n");
    src.push_str("use std::sync::OnceLock;\n\n");

    for (index, entry) in refs.iter().enumerate() {
        let include_path = format!(
            "../../embedded/animations/{}",
            escape_str(&entry.embedded_rel_path)
        );
        let _ = writeln!(
            src,
            "static ANIM_SRC_{index}: &str = include_str!(\"{include_path}\");"
        );
        let _ = writeln!(
            src,
            "static ANIM_CLIP_{index}: OnceLock<Option<AnimationClip>> = OnceLock::new();"
        );
    }
    if !refs.is_empty() {
        src.push('\n');
    }

    src.push_str("fn load_clip(index: usize) -> Option<&'static AnimationClip> {\n");
    src.push_str("    match index {\n");
    for (index, _) in refs.iter().enumerate() {
        let _ = writeln!(
            src,
            "        {index} => ANIM_CLIP_{index}.get_or_init(|| parse_panim(ANIM_SRC_{index}).ok()).as_ref(),"
        );
    }
    src.push_str("        _ => None,\n");
    src.push_str("    }\n");
    src.push_str("}\n\n");

    src.push_str("pub fn lookup_animation(path: &str) -> Option<&'static AnimationClip> {\n");
    src.push_str("    match path {\n");
    for (index, entry) in refs.iter().enumerate() {
        let _ = writeln!(
            src,
            "        \"{}\" => load_clip({index}),",
            escape_str(&entry.lookup_key)
        );
    }
    src.push_str("        _ => None,\n");
    src.push_str("    }\n");
    src.push_str("}\n");

    fs::write(static_dir.join("animations.rs"), src)?;
    Ok(())
}

fn escape_str(input: &str) -> String {
    let mut out = String::with_capacity(input.len());
    for ch in input.chars() {
        match ch {
            '\\' => out.push_str("\\\\"),
            '"' => out.push_str("\\\""),
            '\n' => out.push_str("\\n"),
            '\r' => out.push_str("\\r"),
            '\t' => out.push_str("\\t"),
            _ => out.push(ch),
        }
    }
    out
}
