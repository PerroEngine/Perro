fn emit_bone_target(target: &Option<perro_animation::AnimationBoneTarget>) -> String {
    match target {
        Some(target) => format!(
            "Some(AnimationBoneTarget {{ selector: {} }})",
            emit_bone_selector(&target.selector)
        ),
        None => "None".to_string(),
    }
}

fn emit_bone_selector(selector: &AnimationBoneSelector) -> String {
    match selector {
        AnimationBoneSelector::Index(v) => format!("AnimationBoneSelector::Index({v})"),
        AnimationBoneSelector::Name(v) => format!(
            "AnimationBoneSelector::Name(Cow::Borrowed(\"{}\"))",
            escape_str(v.as_ref())
        ),
    }
}

fn emit_interpolation(value: perro_animation::AnimationInterpolation) -> String {
    format!("AnimationInterpolation::{value:?}")
}

fn emit_ease(value: perro_animation::AnimationEase) -> String {
    format!("AnimationEase::{value:?}")
}

fn emit_event_scope(scope: &AnimationEventScope) -> String {
    match scope {
        AnimationEventScope::Global => "AnimationEventScope::Global".to_string(),
        AnimationEventScope::Object(object) => format!(
            "AnimationEventScope::Object(Cow::Borrowed(\"{}\"))",
            escape_str(object.as_ref())
        ),
    }
}

fn emit_f32(value: f32) -> String {
    format!("f32::from_bits(0x{:08X})", value.to_bits())
}

fn sanitize_ident(path: &str) -> String {
    let mut out = String::new();
    for c in path.chars() {
        if c.is_ascii_alphanumeric() {
            out.push(c.to_ascii_uppercase());
        } else {
            out.push('_');
        }
    }
    out
}

fn escape_str(input: &str) -> String {
    let mut out = String::with_capacity(input.len());
    for ch in input.chars() {
        match ch {
            '\\' => out.push_str("\\\\"),
            '"' => out.push_str("\\\""),
            '\n' => out.push_str("\\n"),
            '\r' => out.push_str("\\r"),
            '\t' => out.push_str("\\t"),
            _ => out.push(ch),
        }
    }
    out
}

