use crate::{
    StaticPipelineError, asset_uri, embedded_dir, ensure_unique_hashes, res_dir, static_dir,
    write_hash_const, write_static_lookup_fn,
};
use perro_asset_formats::source_ext;
use perro_io::walkdir::collect_file_paths;
use std::{fmt::Write as _, fs, path::Path};

pub fn generate_static_shaders(project_root: &Path) -> Result<(), StaticPipelineError> {
    let res_dir = res_dir(project_root);
    let static_dir = static_dir(project_root);
    let embedded_shaders_dir = embedded_dir(project_root).join("shaders");
    fs::create_dir_all(&static_dir)?;
    fs::create_dir_all(&embedded_shaders_dir)?;

    let mut shader_paths = Vec::<String>::new();
    if res_dir.exists() {
        shader_paths = collect_file_paths(&res_dir, &res_dir)?
            .into_iter()
            .map(|rel| rel.replace('\\', "/"))
            .filter(|rel| {
                Path::new(rel)
                    .extension()
                    .and_then(|e| e.to_str())
                    .is_some_and(|ext| source_ext::contains(source_ext::SHADER, ext))
            })
            .collect();
    }
    shader_paths.sort();

    let mut shaders = Vec::<(String, String)>::with_capacity(shader_paths.len());
    for rel in shader_paths {
        let res_path = asset_uri(&rel);
        let full_path = res_dir.join(&rel);
        let source = fs::read_to_string(&full_path)?;

        let output_path = embedded_shaders_dir.join(&rel);
        if let Some(parent) = output_path.parent() {
            fs::create_dir_all(parent)?;
        }
        fs::write(&output_path, source)?;
        shaders.push((res_path, rel));
    }

    shaders.sort_by(|a, b| a.0.cmp(&b.0));
    ensure_unique_hashes("shader", shaders.iter().map(|(path, _)| path.as_str()))?;

    let mut out = String::new();
    out.push_str("// Auto-generated by Perro Static Pipeline. Do not edit.\n");
    out.push_str("#![allow(unused_imports)]\n\n");
    for (index, (_res_path, rel)) in shaders.iter().enumerate() {
        let include_path = format!("../../embedded/shaders/{}", escape_str(rel));
        let _ = writeln!(
            out,
            "static SHADER_{index}: &str = include_str!(\"{include_path}\");"
        );
    }
    if !shaders.is_empty() {
        out.push('\n');
    }
    out.push_str("static EMPTY_SHADER: &str = \"\";\n\n");
    for (index, (res_path, _)) in shaders.iter().enumerate() {
        write_hash_const(&mut out, &format!("SHADER_HASH_{index}"), res_path);
    }
    if !shaders.is_empty() {
        out.push('\n');
    }
    let lookup_entries = shaders
        .iter()
        .enumerate()
        .map(|(index, (res_path, _))| {
            (
                perro_ids::string_to_u64(res_path),
                format!("SHADER_HASH_{index}"),
                format!("SHADER_{index}"),
            )
        })
        .collect::<Vec<_>>();
    write_static_lookup_fn(
        &mut out,
        "lookup_shader",
        "SHADER_TABLE",
        "ShaderEntry",
        "&'static str",
        "EMPTY_SHADER",
        &lookup_entries,
    );

    fs::write(static_dir.join("shaders.rs"), out)?;
    crate::record_static_assets(
        perro_asset_formats::dlc::DlcAssetKind::SHADER,
        perro_asset_formats::dlc::DlcAssetAccess::BYTES,
        shaders.iter().map(|(path, _)| (path.as_str(), false)),
    );
    Ok(())
}

fn escape_str(input: &str) -> String {
    let mut out = String::with_capacity(input.len());
    for ch in input.chars() {
        match ch {
            '\\' => out.push_str("\\\\"),
            '"' => out.push_str("\\\""),
            '\n' => out.push_str("\\n"),
            '\r' => out.push_str("\\r"),
            '\t' => out.push_str("\\t"),
            _ => out.push(ch),
        }
    }
    out
}

#[cfg(test)]
mod tests {
    use super::generate_static_shaders;
    use crate::{StaticPipelineOverrides, set_static_pipeline_overrides};
    use std::{
        fs,
        path::PathBuf,
        time::{SystemTime, UNIX_EPOCH},
    };

    fn unique_temp_root(name: &str) -> PathBuf {
        let stamp = SystemTime::now()
            .duration_since(UNIX_EPOCH)
            .expect("system clock")
            .as_nanos();
        std::env::temp_dir().join(format!("{name}_{stamp}"))
    }

    #[test]
    fn static_shader_embed_preserves_disk_wgsl_exactly() {
        let root = unique_temp_root("perro_static_shader_exact");
        let res_dir = root.join("res");
        let embedded_dir = root.join("embedded");
        let static_dir = root.join("static");
        fs::create_dir_all(res_dir.join("shaders")).expect("create shader dir");
        let source = r#"// keep comments + layout
fn shade_material(in: FragmentInput) -> vec4<f32> {
    let s = "// not a comment";
    return vec4<f32>(in.uv, f32(s == s), 0.5);
}
"#;
        fs::write(res_dir.join("shaders/custom.wgsl"), source).expect("write shader");

        set_static_pipeline_overrides(Some(StaticPipelineOverrides {
            res_dir: res_dir.clone(),
            static_dir,
            embedded_dir: embedded_dir.clone(),
            asset_prefix: "res://".to_string(),
        }));
        let result = generate_static_shaders(&root);
        set_static_pipeline_overrides(None);
        result.expect("generate static shaders");

        let embedded = fs::read_to_string(embedded_dir.join("shaders/shaders/custom.wgsl"))
            .expect("read embedded");
        assert_eq!(embedded, source);
        let _ = fs::remove_dir_all(root);
    }
}
